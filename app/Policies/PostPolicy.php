    }

    /**
     * Determine whether the user can create models.
     *
     * @param  \App\Models\User  $user
     * @return mixed
     */
    public function create(User $user)
    {
        if ($user->roles->contains('slug', 'redactor')) {
            return true;
        }elseif($user->permissions->contains('slug', 'crear-posts')){
            return true;
        }
        return false;
    }

    /**
     * Undocumented function
     *
     * @param User $user
     * @param Post $post
     * @return void
     */
    public function edit(User $user, Post $post)
    {
        if($user->permissions->contains('slug', 'editar-posts')) {
            return true;
        } elseif ($user->roles->contains('slug', 'editor')) {
            return true;
        }
    }

    /**
     * Determine whether the user can update the model.
     *
     * @param  \App\Models\User  $user
     * @param  \App\Models\Post  $post
     * @return mixed
     */
    public function update(User $user, Post $post)
    {
        if($user->roles->contains('slug', 'editor')){
            return true;
        } elseif($user->permissions->contains('slug', 'editar-posts')) {
            return true;
        } elseif($post->userId == $user->id) {
            return true;
        }

        return false;
    }

    /**
     * Determine whether the user can delete the model.
     *
     * @param  \App\Models\User  $user
     * @param  \App\Models\Post  $post
     * @return mixed
     */
    public function delete(User $user, Post $post)
    {
        if($user->permissions->contains('slug', 'borrar-posts')) {
            return true;
        } elseif ($user->roles->contains('slug', 'editor')) {
            return true;
        }
        return false;
    }

